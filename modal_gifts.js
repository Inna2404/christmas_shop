function openModal(event, giftiId) {
  event.preventDefault(); // Запобігає переходу по посиланню

  const gift = giftData.find((item) => item.id === giftiId);

  if (!gift) return;

  const modal = document.getElementById("giftModal");
  modal.style.display = "flex";

  if (gift.category === "For health") {
    modal.style.color = "#06a44f";
  } else if (gift.category === "For work") {
    modal.style.color = "#4361ff";
  } else if (gift.category === "For harmony") {
    modal.style.color = "#ff43f7";
  }
  const modalImage = document.getElementById("modalImage");
  const modalTitle = document.getElementById("modalTitle");
  const modalCategory = document.getElementById("modalCategory");
  const modalDescription = document.getElementById("modalDescription");
  const superpowersBlock = document.getElementById("superpowersBlock");

  modalImage.src = gift.image;
  modalTitle.textContent = gift.title;
  modalCategory.textContent = gift.category;
  modalDescription.textContent = gift.description;

  superpowersBlock.innerHTML = gift.superpowers
    .map(
      (sp) => `
  <div style="display: flex; justify-content: space-between;">
    <p>${sp.name}</p> 
    <div style="display: flex; gap: 5px; justify-content:flex-end; align-items: center;">
    <p> ${sp.power}</p>
      ${
        sp.images && sp.images.length > 0
          ? sp.images
              .map(
                (image) =>
                  `<img src="${image}" alt="snowflake" style="width: 16px; height: 16px;" />`
              )
              .join("")
          : "No images available"
      }
    </div>
  </div>
`
    )
    .join("");
}
function closeModal() {
  const modal = document.getElementById("giftModal");
  modal.style.display = "none";
}
